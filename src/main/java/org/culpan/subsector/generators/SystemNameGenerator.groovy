    def static getSystemName() {
        def num = DiceRoller.totalDice(1, 11)
        switch (num) {
            case 1:
                return f[DiceRoller.totalDice(1, f.size()) - 1] + d[DiceRoller.totalDice(1, d.size()) - 1]
            case 2:
                return h[DiceRoller.totalDice(1, h.size()) - 1] + i[DiceRoller.totalDice(1, i.size()) - 1]
            case 3:
                return c[DiceRoller.totalDice(1, c.size()) - 1] + d[DiceRoller.totalDice(1, d.size()) - 1]
            case 4:
                return c[DiceRoller.totalDice(1, c.size()) - 1] + f[DiceRoller.totalDice(1, f.size()) - 1] + e[DiceRoller.totalDice(1, e.size()) - 1]
            case 5:
                return h[DiceRoller.totalDice(1, h.size()) - 1] + g[DiceRoller.totalDice(1, g.size()) - 1] + e[DiceRoller.totalDice(1, e.size()) - 1]
            case 6:
                return c[DiceRoller.totalDice(1, c.size()) - 1] + j[DiceRoller.totalDice(1, j.size()) - 1] + i[DiceRoller.totalDice(1, i.size()) - 1]
            case 7:
                return c[DiceRoller.totalDice(1, c.size()) - 1] + e[DiceRoller.totalDice(1, e.size()) - 1]
            case 8:
                return f[DiceRoller.totalDice(1, f.size()) - 1] + e[DiceRoller.totalDice(1, e.size()) - 1]
            case 9:
                return h[DiceRoller.totalDice(1, h.size()) - 1] + i[DiceRoller.totalDice(1, i.size()) - 1]
            case 10:
                return c[DiceRoller.totalDice(1, c.size()) - 1] + d[DiceRoller.totalDice(1, d.size()) - 1]
            default:
                return m[DiceRoller.totalDice(1, m.size()) - 1] + l[DiceRoller.totalDice(1, l.size()) - 1]
        }
    }


